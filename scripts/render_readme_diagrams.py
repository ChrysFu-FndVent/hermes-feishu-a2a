def text(lines: list[str], x: int, y: int, css: str, gap: int = 24, anchor: str = "start") -> str:
    spans = "".join(
        f'<tspan x="{x}" dy="{0 if index == 0 else gap}">{escape(line)}</tspan>'
        for index, line in enumerate(lines)
    )
    return f'<text x="{x}" y="{y}" class="{css}" text-anchor="{anchor}">{spans}</text>'


def card(
    x: int,
    y: int,
    width: int,
    height: int,
    accent: str,
    eyebrow: str,
    heading: str,
    lines: list[str],
) -> list[str]:
    return [
        f'<g filter="url(#shadow)"><rect x="{x}" y="{y}" width="{width}" height="{height}" rx="18" '
        f'fill="{PALETTE["surface"]}" stroke="{PALETTE["border"]}" stroke-width="2"/>',
        f'<rect x="{x}" y="{y}" width="7" height="{height}" rx="4" fill="{accent}"/>',
        f'<circle cx="{x + 34}" cy="{y + 35}" r="8" fill="{accent}"/>',
        f'<text x="{x + 54}" y="{y + 40}" class="eyebrow" fill="{accent}">{escape(eyebrow.upper())}</text>',
        f'<text x="{x + 28}" y="{y + 78}" class="heading">{escape(heading)}</text>',
        text(lines, x + 28, y + 108, "body", 28),
        "</g>",
    ]


