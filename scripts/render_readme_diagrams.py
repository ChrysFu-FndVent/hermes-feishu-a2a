def pill(x: int, y: int, width: int, label: str, color: str) -> str:
    return (
        f'<g><rect x="{x}" y="{y}" width="{width}" height="30" rx="15" fill="{PALETTE["surface_2"]}" '
        f'stroke="{color}"/><rect x="{x}" y="{y}" width="{width}" height="30" rx="15" fill="{color}" opacity=".12"/>'
        f'<text x="{x + width / 2}" y="{y + 21}" class="pill" fill="{color}" '
        f'text-anchor="middle">{escape(label.upper())}</text></g>'
    )


